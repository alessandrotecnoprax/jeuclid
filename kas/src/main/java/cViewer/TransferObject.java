package cViewer;

public class TransferObject {

    private String content;

    /**
     * Getter method for content.
     * 
     * @return the content
     */
    public String getContent() {
        return this.content;
    }

    /**
     * Setter method for content.
     * 
     * @param content
     *            the content to set
     */
    public void setContent(final String content) {
        this.content = content;
    }

}