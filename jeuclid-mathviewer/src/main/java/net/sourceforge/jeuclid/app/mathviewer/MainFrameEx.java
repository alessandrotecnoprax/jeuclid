    private void insertMacro(String text)
    {
        int pos = getXMLEditor().getCaretPosition();
        String s1 = getXMLEditor().getText().substring(0,pos);
        String s2 = getXMLEditor().getText().substring(pos);
        s1 += text + s2;
        getXMLEditor().setText(s1);
        updateFromTextArea();
    }
